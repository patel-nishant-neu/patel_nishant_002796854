    private void addPlanBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addPlanBtnActionPerformed
        // TODO add your handling code here:
        PlanDetail planCatalog = this.business.getInsuranceDetail();
        
        String planID = String.valueOf(fieldInsurancePLanID.getText());
        String planName = fieldInsurancePlanName.getText();
        String planCostpm = String.valueOf(fieldCostPM.getText());
        
        String planCostpa = String.valueOf(insurance.calulatePlanCostPerAnnum(Double.valueOf(fieldCostPM.getText())));
        fieldCostPA.setText(planCostpa);
        
        planCatalog.createInsurancePlan(Integer.valueOf(planID), planName, Double.valueOf(planCostpm));
        
        JOptionPane.showMessageDialog(null, "Successfully added Insurance Plan to System!");
        
        fieldInsurancePlanName.setText("");
        fieldInsurancePLanID.setText("");
        fieldCostPM.setText("");     
        fieldCostPA.setText("");
    }//GEN-LAST:event_addPlanBtnActionPerformed

