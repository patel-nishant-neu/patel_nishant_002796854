    private void fieldInsurancePLanIDFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_fieldInsurancePLanIDFocusLost
        // TODO add your handling code here:
      
        PlanDetail planCatalog = this.business.getInsuranceDetail();
        
        if(planCatalog.checkInsuranceIDUnique(Integer.valueOf(fieldInsurancePLanID.getText())) == false){
            JOptionPane.showMessageDialog(null, "Application ID is already taken. Please enter unique ID!");
            fieldInsurancePLanID.setText("");
            fieldInsurancePLanID.requestFocus();
        }     
        
    }//GEN-LAST:event_fieldInsurancePLanIDFocusLost


