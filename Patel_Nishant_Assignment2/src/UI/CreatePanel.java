    private void fieldAppIDFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_fieldAppIDFocusLost
        // TODO add your handling code here:
        
        ApplicantCatalog appCatalog = this.business.getApplicantCatalog();
        
        if (fieldAppID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter some text!");
            fieldAppID.requestFocus();
        }   
        
        if(appCatalog.checkApplicationIDUnique(Integer.valueOf(fieldAppID.getText())) == false){
            JOptionPane.showMessageDialog(null, "Application ID is already taken. Please enter unique ID!");
            fieldAppID.setText("");
            fieldAppID.requestFocus();
        }     
        
        
    }//GEN-LAST:event_fieldAppIDFocusLost


