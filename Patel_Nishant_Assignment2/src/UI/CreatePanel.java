    private void CreateDetailsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateDetailsBtnActionPerformed
        // TODO add your handling code here:
        
        String petVaccineCourseStatus;
        ApplicantCatalog appCatalog = this.business.getApplicantCatalog();
        
        String appID = fieldAppID.getText();
        String firstname = fieldOwnerFirstname.getText();
        String lastname = fieldOwnerLastName.getText();
        
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        String app_date = sdf.format(jDateChooser2.getDate());
        
//        System.out.println(app_date);
        
        String petType = typeComboBox.getSelectedItem().toString();
        String petname = fieldPetName.getText();
        String petBreed = fieldPetBreed.getText();
        String petGender = jComboBox1.getSelectedItem().toString();
        
//        System.out.println(petGender);
        
        String petAge = fieldPetAge.getText();
        
        String petVaccine = fieldPetVaccine.getText();
        if(petVaccine.isEmpty()){
            fieldNoOfVax.setText(String.valueOf(0));
        }else{
            String[] values = petVaccine.split(",");
            fieldNoOfVax.setText(String.valueOf(values.length));      
        }
        
//        System.out.println("value is" + fieldNoOfVax.getText());


        if (jCheckBox1.isSelected()) {
            petVaccineCourseStatus = "Yes";
        } else {
            petVaccineCourseStatus = "No";
        } 

        ArrayList<JTextField> textFields = new ArrayList<JTextField>();
        textFields.add(fieldAppID);
        textFields.add(fieldOwnerFirstname);
        textFields.add(fieldOwnerLastName);
        textFields.add(fieldPetAge);
        textFields.add(fieldPetBreed);
        textFields.add(fieldPetName);
        
        boolean allFieldsFilled = true;

        for (JTextField textField : textFields) {
            if(textField.getText().isEmpty()){
                allFieldsFilled = false;
                break;
            }
        }
        
        if(allFieldsFilled){
   
            appCatalog.createApplicant(firstname, lastname, Integer.valueOf(appID), petname, petType, petBreed, petGender, Integer.valueOf(petAge), petVaccine, petVaccineCourseStatus, app_date);

            JOptionPane.showMessageDialog(null, "Successfully added Applicant to Catalog");  
  
            fieldAppID.setText("");
            fieldOwnerFirstname.setText("");
            fieldOwnerLastName.setText("");

            typeComboBox.setSelectedItem(null);
            fieldPetName.setText("");
            fieldPetBreed.setText("");
            jComboBox1.setSelectedItem(null);
            fieldPetAge.setText("");
            jDateChooser2.setDate(null);
            fieldNoOfVax.setText("");

            fieldPetVaccine.setText("");
            jCheckBox1.setSelected(false);
            
        }else{
            JOptionPane.showMessageDialog(null, "Fields cannot be empty! ");  
        }
        
        
    }//GEN-LAST:event_CreateDetailsBtnActionPerformed

