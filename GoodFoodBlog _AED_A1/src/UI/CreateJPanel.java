    private void jCreateChefBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCreateChefBtnActionPerformed
        // TODO add your handling code here:

        String chefFirstName = chefFName1.getText();
        String chefLastName = chefLName1.getText();
        String chefUsername = chefUName.getText();
        String chefEmailID = chefEmail.getText();
        String chefPhoneNum = chefPhone.getText();

        // store the data

        chef chef = this.recipe.getChef();
        chef.setChefFName(chefFirstName);
        chef.setChefLName(chefLastName);
        chef.setUsername(chefUsername);
        chef.setEmailID(chefEmailID);
        chef.setPhoneNum(Integer.valueOf(chefPhoneNum));

        if(validate) {
            JOptionPane.showMessageDialog(null, "Pleae fill all fields");
        } else {
            JOptionPane.showMessageDialog(null, "Saved!");
        }
    }//GEN-LAST:event_jCreateChefBtnActionPerformed

